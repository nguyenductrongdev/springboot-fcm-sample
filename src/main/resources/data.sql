-- books seeding
CREATE TABLE IF NOT EXISTS books (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255),
    pages INTEGER,
    deletedAt TIMESTAMP
);

